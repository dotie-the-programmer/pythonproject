#Arithmetic operators
num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Modulus- Returns the remainder.

#Comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #Equal to
print(num1 != num2) #Not equal to

#Assignment Operators
number = 12
print(number)

number += 2
print(number)

b = 56
b /= 1
print(b)

#Logic operators - and, or, not
print(34 < 10 and 89 > 5)
print(34 < 10 or 89 > 5)
print(not(34 < 10 or 89 > 5))


