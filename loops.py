# While loop

number = 20
while number <= 25:
    print(number)
    number += 1 # incrementing

# Decrement
count = 105
while count >= 100:
    print("number is", count)
    count -= 1

# For loop
for num in range(1, 5):
    print(num)

for x in range(1, 10, 2):
    print(x)

#list
languages = ["python","kotlin","PHP"]
for lang in languages:
    print(lang)


